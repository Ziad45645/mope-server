    public static AnimalInfo byName(String name) {
        try {
            Field[] fields = AnimalSkins.class.getDeclaredFields();
            for (Field f : fields) {
                StringBuilder strBuilder = new StringBuilder("");
                strBuilder.append(name).append("_skin");
                if (Modifier.isStatic(f.getModifiers()) && f.getName().equalsIgnoreCase(strBuilder.toString())) {
                    Class<?> type = f.getType();
                    return new AnimalInfo((AnimalInfo) f.get(type));
                }
            }
            return null;
        } catch(IllegalAccessException e){
            return null;
        }
    }
